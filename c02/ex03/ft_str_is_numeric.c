int		ft_str_is_numeric(char *str)
{
	int i;
	int ret;

	i = 0;
	if (str[0] == '\0')
	{
		ret = 1;
	}
	else
	{
		while (str[i] != '\0')
		{
			if (str[i] > 47 && str[i] < 58)
			{
				ret = 1;
				i++;
			}
			else
			{
				ret = 0;
				break ;
			}
		}
	}
	return (ret);
}
