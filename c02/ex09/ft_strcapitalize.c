char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	if (str[0] > 96 && str[0] < 123)
	{
		str[0] = str[0] - 32;
	}
	while (str[i] != '\0')
	{
		i++;
		if (str[i - 1] > 31 && str[i - 1] < 48)
		{
			if (str[i] > 96 && str[i] < 123)
			{
				str[i] = str[i] - 32;
			}
		}
		else if (str[i] > 64 && str[i] < 91)
		{
			str[i] = str[i] + 32;
		}
	}
	return (str);
}
