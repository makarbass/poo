int		ft_str_is_printable(char *str)
{
	int i;
	int ret;

	i = 0;
	if (str[0] == '\0')
	{
		ret = 1;
	}
	else
	{
		while (str[i] != '\0')
		{
			if (str[i] > 31 && str[i] < 128)
			{
				ret = 1;
				i++;
			}
			else
			{
				ret = 0;
				break ;
			}
		}
	}
	return (ret);
}
