int	ft_sqrt(int nb)
{
	int i;
	int count;

	i = 1;
	count = 0;
	while (nb > 0)
	{
		nb = nb - i;
		i += 2;
		count++;
	}
	if (nb != 0)
		return (0);
	return (count);
}
