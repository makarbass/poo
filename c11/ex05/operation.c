int	summ(int a, int b)
{
	return (a + b);
}

int	razn(int a, int b)
{
	return (a - b);
}

int	umn(int a, int b)
{
	return (a * b);
}

int	del(int a, int b)
{
	return (a / b);
}

int	mod(int a, int b)
{
	return (a % b);
}
